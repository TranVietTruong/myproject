    private void btn_FillterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_FillterMouseClicked
        try {
            String from  = ((JTextField)date_From.getDateEditor().getUiComponent()).getText();
            String to  = ((JTextField)date_To.getDateEditor().getUiComponent()).getText();
            if(from.equals("") || to.equals(""))
            {
                return;
            }
            listBill = billModel.FillterBillByTime(from, to);
            LoadTableBill();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(FrmBill.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_FillterMouseClicked
    // xóa một hóa đơn
    private void btn_DeleteBillMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_DeleteBillMouseClicked
        
        if(dongchon != -1)
        {
            int close;
            close = JOptionPane.showConfirmDialog(null,"Xác nhận xóa","Thông báo",JOptionPane.YES_OPTION,JOptionPane.NO_OPTION);
            if(close == JOptionPane.NO_OPTION)
            {
                return;
            }
            try {
                int id = listBill.get(dongchon).getId();
                if(billModel.DeleteBill(id))
                {
                    listBill.remove(dongchon);
                    LoadTableBill();
                    JOptionPane.showMessageDialog(null,"Xóa thành công");
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"Xóa thất bại");
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(FrmBill.class.getName()).log(Level.SEVERE, null, ex);
            }
            dongchon = -1;
        }
    }//GEN-LAST:event_btn_DeleteBillMouseClicked

