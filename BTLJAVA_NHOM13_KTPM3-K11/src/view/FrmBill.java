    private void tb_BillMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_BillMouseReleased
        dongchon = tb_Bill.getSelectedRow();
        if(dongchon != -1)
        {
          
            try {
                lb_CodeBill.setText(listBill.get(dongchon).getId()+"");
                lb_NameCustomer.setText(listBill.get(dongchon).getName_customer());
                lb_NameEmployee.setText(listBill.get(dongchon).getName_employee());
                lb_DateOrder.setText(listBill.get(dongchon).getDate_order());
                lb_TotalMoney.setText(numf.format(listBill.get(dongchon).getTota_money())+"");
                lb_Promotion.setText(numf.format(listBill.get(dongchon).getPromotion())+"");
                lb_PayMoney.setText(numf.format(listBill.get(dongchon).getTota_money() - listBill.get(dongchon).getPromotion()) +"");
                LoadListOrderById(listBill.get(dongchon).getId());
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(FrmBill.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tb_BillMouseReleased

